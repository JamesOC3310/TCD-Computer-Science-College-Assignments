# N-gram model class
# looks at previous (n-1) tokens to predict next item in melody sequence
class NGramModel:
    def __init__(self, n):
        self.n = n
        self.ngrams = defaultdict(Counter)

    def train(self, tokens):
        for i in range(len(tokens) - self.n):
            context = tuple(tokens[i:i + self.n - 1])
            next_token = tokens[i + self.n - 1]
            self.ngrams[context][next_token] += 1

    def generate(self, start_tokens, length):
        current_context = tuple(start_tokens[-(self.n - 1):])
        generated = list(start_tokens)

        for _ in range(length):
            if current_context in self.ngrams:
                next_token = self._sample_next_token(current_context)
            else:
                next_token = random.choice(list(self.ngrams.keys()))[0]

            generated.append(next_token)
            current_context = tuple(generated[-(self.n - 1):])

        return generated

    def _sample_next_token(self, context):
        next_tokens = self.ngrams[context]
        total = sum(next_tokens.values())
        choices, weights = zip(*next_tokens.items())
        return random.choices(choices, weights=[w / total for w in weights])[0]

    def calculate_perplexity(self, tokens):
        log_prob_sum = 0
        num_tokens = 0

        for i in range(len(tokens) - self.n):
            context = tuple(tokens[i:i + self.n - 1])
            next_token = tokens[i + self.n - 1]

            if context in self.ngrams and next_token in self.ngrams[context]:
                prob = self.ngrams[context][next_token] / sum(self.ngrams[context].values())
                log_prob_sum += -torch.log2(torch.tensor(prob))
                num_tokens += 1

        perplexity = torch.exp2(log_prob_sum / num_tokens) if num_tokens > 0 else float('inf')
        return perplexity


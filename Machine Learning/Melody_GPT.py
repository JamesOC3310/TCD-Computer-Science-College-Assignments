# List of notes in order
NOTES = ['C', 'c', 'D', 'd', 'E', 'F', 'f', 'G', 'g', 'A', 'a', 'B']

def translate_notes(notes, shift):
    translated_notes = []
    for note in notes:
        if note in NOTES:
            index = NOTES.index(note)
            new_index = (index + shift) % len(NOTES)
            translated_notes.append(NOTES[new_index])
        else:
            translated_notes.append(note)  # Keep the character as is if it's not a note
    return ''.join(translated_notes)


# add rhythm variations function
def rhythm_variations(sequence, rest_probability=0.2):
  """
  introduces rhythm variations by adding random rests between notes

  args
  sequence(str): original melody as space-separated string of notes
  rest_probability(float): probability of adding a rest between notes

  returns
  str: rhythm variations melody
  """
  modified_sequence = []
  for token in sequence.split():
    modified_sequence.append(token)
    # add random rests
    if random.random() < rest_probability:
      modified_sequence.append('R')
  return ' '.join(modified_sequence)

def group_into_phrases(sequence, phrase_length=4):
  """
  group notes into phrases of specified lengh
  args
  sequence(str): original melody as space-separated string of notes
  phrase_length(int): number of notes per phrase

  helps model learn relationships between groups of notes (phrases)
  rather than individual notes

  returns
  str: melody grouped into phrases
  """
  tokens = sequence.split()
  phrases = [''.join(tokens[i:i+phrase_length]) for i in range(0, len(tokens), phrase_length)]
  return ' '.join(phrases)

def compact_tokenization(sequence):
    """
    shortens repeated notes in the sequence

    Args:
    sequence (str): melody as space-separated string of notes

    Returns:
    str: shortened tokenized melody
    """
    if not sequence.strip():  # handle empty or whitespace-only input
        return ""

    tokens = sequence.split()  # split melody into individual notes
    compacted = []

    for token, group in groupby(tokens):
        count = len(list(group))  # count consecutive occurrences of the token
        if count > 1:
            compacted.append(f"{token}*{count}")
        else:
            compacted.append(token)

    return ' '.join(compacted)

def reverse_melody(sequence):
    """
    reverses the order of the notes in the melody.

    Args:
        sequence (str): Melody as a space-separated string of notes.

    Returns:
        str: Reversed melody sequence.
    """
    return ' '.join(sequence.split()[::-1])

def scale_pitch(sequence, scale_factor):
    """
    Scales the pitch of the notes in the sequence by a given factor.

    Args:
        sequence (str): Melody as a space-separated string of notes.
        scale_factor (int): Number of semitones to scale (positive or negative).

    Returns:
        str: Scaled melody sequence.
    """
    scaled_sequence = []
    for token in sequence.split():
        if token in NOTES:  # Only scale valid notes
            index = NOTES.index(token)
            new_index = (index + scale_factor) % len(NOTES)
            scaled_sequence.append(NOTES[new_index])
        else:
            scaled_sequence.append(token)  # Leave rests or unknown tokens unchanged
    return ' '.join(scaled_sequence)



def calculate_diversity_and_repetition(tokens, n=3):
    # Diversity: Unique token ratio
    unique_tokens = len(set(tokens))
    total_tokens = len(tokens)
    unique_ratio = unique_tokens / total_tokens if total_tokens > 0 else 0

    # Repetition: Count repeated n-grams
    ngrams = [tuple(tokens[i:i + n]) for i in range(len(tokens) - n + 1)]
    ngram_counts = Counter(ngrams)
    repeated_ngrams = sum(count - 1 for count in ngram_counts.values() if count > 1)
    repetition_ratio = repeated_ngrams / len(ngrams) if len(ngrams) > 0 else 0

    return unique_ratio, repetition_ratio

def calculate_shannon_entropy(tokens):
    counts = Counter(tokens)
    probabilities = [count / sum(counts.values()) for count in counts.values()]
    return -sum(p * np.log2(p) for p in probabilities if p > 0)

def calculate_pitch_and_rhythm_similarity(original, generated):
    # Count occurrences of pitch and rhythm tokens
    original_counts = Counter(original)
    generated_counts = Counter(generated)

    # Normalize to get probabilities
    total_original = sum(original_counts.values())
    total_generated = sum(generated_counts.values())

    original_dist = {k: v / total_original for k, v in original_counts.items()}
    generated_dist = {k: v / total_generated for k, v in generated_counts.items()}

    # KL Divergence
    kl_div = calculate_kl_divergence(original_dist, generated_dist)

    # Earth Mover's Distance (using scipy)
    original_array = np.array([[original_dist.get(k, 0)] for k in original_dist.keys()])
    generated_array = np.array([[generated_dist.get(k, 0)] for k in original_dist.keys()])
    emd = cdist(original_array, generated_array, metric='euclidean').sum()

    return kl_div, emd

def calculate_transition_matrix(tokens, vocab_size):
    matrix = np.zeros((vocab_size, vocab_size))
    for i in range(len(tokens) - 1):
        matrix[tokens[i], tokens[i + 1]] += 1
    row_sums = matrix.sum(axis=1, keepdims=True)
    return matrix / np.maximum(row_sums, 1)  # Normalize rows to probabilities

def calculate_compression_ratio(sequence):
    original_size = len(sequence)
    compressed_size = len(zlib.compress(bytes(sequence)))
    return compressed_size / original_size

def calculate_self_similarity_matrix(sequence):
    length = len(sequence)
    matrix = np.zeros((length, length))
    for i in range(length):
        for j in range(length):
            matrix[i, j] = 1 if sequence[i] == sequence[j] else 0
    return matrix


# Function to calculate token distribution
def calculate_token_distribution(tokens):
    counter = Counter(tokens)
    total = sum(counter.values())
    return {token: count / total for token, count in counter.items()}

# Function to calculate KL Divergence
def calculate_kl_divergence(p, q):
    kl_div = 0
    for token, p_val in p.items():
        q_val = q.get(token, 1e-8)  # Handle missing tokens with smoothing
        kl_div += p_val * np.log(p_val / q_val)
    return kl_div

# Function to calculate diversity and repetition
def calculate_diversity_and_repetition(tokens, n=3):
    # Diversity: Unique token ratio
    unique_tokens = len(set(tokens))
    total_tokens = len(tokens)
    unique_ratio = unique_tokens / total_tokens if total_tokens > 0 else 0

    # Repetition: Count repeated n-grams
    ngrams = [tuple(tokens[i:i + n]) for i in range(len(tokens) - n + 1)]
    ngram_counts = Counter(ngrams)
    repeated_ngrams = sum(count - 1 for count in ngram_counts.values() if count > 1)
    repetition_ratio = repeated_ngrams / len(ngrams) if len(ngrams) > 0 else 0

    return unique_ratio, repetition_ratio



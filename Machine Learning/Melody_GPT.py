def calculate_sdi(input_melody, output_melody):
    """Calculate Sequence Divergence Index (SDI)"""
    # tokenize melodies into unique sets of notes
    input_set = set(input_melody)
    output_set = set(output_melody)

    # calculate Jaccard similarity
    intersection = len(input_set.intersection(output_set))
    union = len(input_set.union(output_set))
    jaccard_similarity = intersection / union if union > 0 else 0

    # SDI is 1 - Jaccard similarity
    sdi = 1 - jaccard_similarity
    return sdi

def calculate_rcs(melody):
    """Calculate Rhythmic Complexity Score (RCS)"""
    # count consecutive occurrences of each note (simulating rhythm)
    rhythm = []
    current_note = None
    count = 0

    for note in melody:
        if note == current_note:
            count += 1
        else:
            if current_note is not None:
                rhythm.append(count)
            current_note = note
            count = 1
    if count > 0:
        rhythm.append(count)

    # Calculate entropy
    total_rhythm = sum(rhythm)
    probs = [r / total_rhythm for r in rhythm]
    entropy = -sum(p * np.log2(p) for p in probs if p > 0)

    # Normalize by the maximum possible entropy
    max_entropy = np.log2(len(rhythm)) if len(rhythm) > 0 else 1
    rcs = entropy / max_entropy
    return rcs

